-- DATEFROMPARTS Function in SQL -- 

-- in built function in SQL Server

-- it helps to return DATE value which maps to a year, 
--month and day value. 

-- DATEFROMPARTS(year, month, day) 

-- year is the integer which resolves to a year
-- month is an integer expression which evaluates to a month
-- day is interger specifies a day from 1 to 31. 

SELECT DATEFROMPARTS(2022,09,15) today;




