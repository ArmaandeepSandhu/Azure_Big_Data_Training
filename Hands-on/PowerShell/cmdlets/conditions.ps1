# if-else statement 

$x = 30 

if($x -le 20) {
    Write-Host("This is if statement")
}else{
    Write-Host("This is else statement")
}

# else-if statement 

$x = 30

if($x -eq 10){
   write-host("Value of X is 10")
} elseif($x -eq 20){
   write-host("Value of X is 20")
} elseif($x -eq 30){
   write-host("Value of X is 30")
} else {
   write-host("This is else statement")
}